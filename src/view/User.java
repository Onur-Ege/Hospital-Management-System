    private static AccountType getAccountTypeFromString(String accountString){
        
        for(AccountType accountType:AccountType.values()){
            if(accountType.name().equalsIgnoreCase(accountString)){
                return accountType;
            }
        }
        throw new IllegalArgumentException("invalid account string" + accountString);
    }
    
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String selectedRole = (String) ComRole.getSelectedItem();
        AccountType accountType = getAccountTypeFromString(selectedRole);
        
        Userdto user = new Userdto(
                txtFirstname.getText(),
                txtLastname.getText(),
                txtEmail.getText().trim().toLowerCase(),
                new PasswordConfig().encrypt(txtPassword.getText()),
                accountType
        );

        try {
            
            Connection conn = DBConn.getInstance().getConnection();
            
            PreparedStatement pst = conn.prepareStatement("insert into user(id,firstname,lastname,email,password,role) values(?,?,?,?,?,?)");
            
            pst.setString(1,generateUserId());
            pst.setString(2,user.getFirstname());
            pst.setString(3,user.getLastname());
            pst.setString(4,user.getEmail());
            pst.setString(5,user.getPassword());
            pst.setString(6,selectedRole);

            
            int save = pst.executeUpdate();
            
            if(save != 0){
                JOptionPane.showMessageDialog(this, "record added");
                txtFirstname.setText("");
                txtLastname.setText("");
                txtEmail.setText("");
                txtPassword.setText("");
                
                if(selectedRole == "Patient"){
                    
                    Patient p = new Patient();
                    p.setVisible(true);
                    this.setVisible(false);
                    
                }else{
                    
                    Doctor d = new Doctor();
                    d.setVisible(true);
                    this.setVisible(false);
                    
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "record failed");
            }
            
            // TODO add your handling code here:
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Doctor.class.getName()).log(Level.SEVERE, null, ex);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton5ActionPerformed

