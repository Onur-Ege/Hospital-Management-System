    private String generateUserId() throws ClassNotFoundException, SQLException{
        
        String query = "SELECT id FROM user ORDER BY id DESC LIMIT 1";
        
        Connection connection = DBConn.getInstance().getConnection();
        Statement st = connection.createStatement();
        ResultSet rs = st.executeQuery(query);
        
        if(rs.next()){
            String selectedId = rs.getString(1);
            String[] splitString = selectedId.split("-");
            String splitId = splitString[1];
            int id = Integer.parseInt(splitId);
            id++;
            return "U-" + id;
        }
        
        return "U-1";
        
    } 

