    public void table() throws ClassNotFoundException, SQLException{
        
        Connection conn = DBConn.getInstance().getConnection();
         
        PreparedStatement pst = conn.prepareStatement("select * from appointment");
        ResultSet rs = pst.executeQuery();
        
        ResultSetMetaData rsm = rs.getMetaData();
        int c = rsm.getColumnCount();  
        DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
        df.setRowCount(0);
        
        while(rs.next()){
            
            Vector v2 = new Vector();
            
            for(int i=1; i<=c; i++){
                
                v2.add(rs.getString("id")); 
                v2.add(rs.getString("date"));    
                v2.add(rs.getString("time"));    
                v2.add(rs.getString("doctorid"));    
                v2.add(rs.getString("patientid"));    
                v2.add(rs.getString("checkstate"));    
            }
            
            df.addRow(v2);
            
        }

    }

